local function getKey(table, value)
    if not value then return end

    for _, v in ipairs(table) do
        if type(v) == "string" and string.lower(v) == string.lower(value) then
            return v
        end
    end
    return nil
end

local function getRandomKey(t)
    local keys = {}
    for _, key in ipairs(t) do
        table.insert(keys, key)
    end
    if #keys > 0 then
        local randomIndex = math.random(1, #keys)
        return keys[randomIndex]
    else
        return nil
    end
end

-- My horse:
-- /horse gang_john 78

-- https://github.com/Rexshack-RedM/rsg-horses/blob/main/shared/horse_comp.lua
RegisterCommand("horse", function(source, args)
    local model = args[1] or nil
    local saddleHash = nil
    local saddleID = args[2] or "rand"
    local scale = args[3]
    if model and model ~= "rand" then
        -- model = getKey(horses, tostring(model))
-- 
        if not IsModelValid(model) then
            TriggerEvent('chat:addMessage', {
                color = { 255, 0, 0 },
                args = { "Server", "Invalid model" }
            })
            return
        end

        model = GetHashKey(model)
    else
        model = getRandomKey(horses)
        print("Model: " .. model)
    end

    -- Get saddle
    if saddleID ~= "rand" then
        local saddle = saddles[tonumber(saddleID)]
        if not saddle then
            TriggerEvent('chat:addMessage', {
                color = { 255, 0, 0 },
                args = { "Server", "Invalid saddle" }
            })
            return
        end

        saddleHash = saddle.hash
    else
        local rnd = math.random(1, #saddles)
        saddleHash = saddles[rnd].hash
        print("Saddle: " .. tostring(rnd))
    end

    -- Get player stuff
    local playerId = PlayerId()
    local playerPed = GetPlayerPed(playerId)

    local horse = GetMount(PlayerPedId())
    if DoesEntityExist(horse) then
        DeleteEntity(horse)
    end

    -- Request the horse model
    RequestModel(model)
    while not HasModelLoaded(model) do
        Wait(0)
    end

    SetRelationshipBetweenGroups(0, GetPedGroupIndex(playerPed), GetPedGroupIndex(horse))

    -- Spawn horse
    local horseCoords = GetEntityCoords(playerPed)
    local horse = CreatePed(model, horseCoords.x, horseCoords.y, horseCoords.z, 0, true, true, true, true)

    Citizen.InvokeNative(0xE6D4E435B56D5BD0, playerId, horse) -- SetPlayerOwnsMount
    Citizen.InvokeNative(0xA69899995997A63B, horse, 4) -- SetMountBondingLevel

    Citizen.InvokeNative(0x283978A15512B2FE, horse, true) -- _SET_RANDOM_OUTFIT_VARIATION

    -- Attach components
    Citizen.InvokeNative(0xD3A7B003ED343FD9, horse, 0xF772CED6, true, true, true) -- _APPLY_SHOP_ITEM_TO_PED (Holster)
    Citizen.InvokeNative(0xD3A7B003ED343FD9, horse, 0xBBF05395, true, true, true) -- _APPLY_SHOP_ITEM_TO_PED (Paddle)
    Citizen.InvokeNative(0xD3A7B003ED343FD9, horse, saddleHash, true, true, true) -- _APPLY_SHOP_ITEM_TO_PED (Saddle)

    if scale then
        SetPedScale(horse, tonumber(scale) + 0.0)
    end

    SetModelAsNoLongerNeeded(model)
    SetEntityAsNoLongerNeeded(horse)

    -- Sit on horse
    Citizen.InvokeNative(0x028F76B6E78246EB, playerPed, horse, -1, true) -- SET_PED_ONTO_MOUNT

    Wait(100)
    print("Restoring Horse")
    exports.player:restoreCores(GetMount(PlayerPedId()), true)
end)

